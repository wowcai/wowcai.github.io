function buildHistoryScores(school, schoolIndex) {
  const yearDelta = [-3, -1, 0, 2, 4];
  return Object.fromEntries(
    GAOKAO_SOURCE_PROVINCES.map((province, provinceIndex) => [
      province.name,
      yearDelta.map((delta, yearIndex) => (
        school.baseScore + province.adjust + delta + ((schoolIndex + provinceIndex + yearIndex) % 3) - 1
      ))
    ])
  );
}

